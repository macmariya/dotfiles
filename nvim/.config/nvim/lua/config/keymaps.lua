-- ==========================================================
-- キーマップ設定
-- ==========================================================

-- リーダーキーをスペースに設定
vim.g.mapleader = " "
vim.g.maplocalleader = " "

local map = vim.keymap.set

-- ── ウィンドウ移動 ──────────────────────────────────────
map("n", "<C-h>", "<C-w>h", { desc = "左のウィンドウへ移動" })
map("n", "<C-j>", "<C-w>j", { desc = "下のウィンドウへ移動" })
map("n", "<C-k>", "<C-w>k", { desc = "上のウィンドウへ移動" })
map("n", "<C-l>", "<C-w>l", { desc = "右のウィンドウへ移動" })

-- ── ウィンドウリサイズ ──────────────────────────────────
map("n", "<C-Up>", "<cmd>resize +2<cr>", { desc = "ウィンドウ高さを増やす" })
map("n", "<C-Down>", "<cmd>resize -2<cr>", { desc = "ウィンドウ高さを減らす" })
map("n", "<C-Left>", "<cmd>vertical resize -2<cr>", { desc = "ウィンドウ幅を減らす" })
map("n", "<C-Right>", "<cmd>vertical resize +2<cr>", { desc = "ウィンドウ幅を増やす" })

-- ── バッファ移動 ────────────────────────────────────────
map("n", "]b", "<cmd>bnext<cr>", { desc = "次のバッファ" })
map("n", "[b", "<cmd>bprevious<cr>", { desc = "前のバッファ" })

-- ── 検索ハイライト消去 ──────────────────────────────────
map("n", "<Esc>", "<cmd>nohlsearch<cr>", { desc = "検索ハイライトを消去" })

-- ── ビジュアルモードでインデント保持 ────────────────────
map("v", "<", "<gv", { desc = "インデントを減らして選択を維持" })
map("v", ">", ">gv", { desc = "インデントを増やして選択を維持" })

-- ── インサートモード離脱 ────────────────────────────────
map("i", "jk", "<Esc>", { desc = "インサートモードを離脱" })

-- ── 行移動（ビジュアルモード） ──────────────────────────
map("v", "J", ":m '>+1<cr>gv=gv", { desc = "選択行を下に移動" })
map("v", "K", ":m '<-2<cr>gv=gv", { desc = "選択行を上に移動" })

-- ── Leaderキーマップ ────────────────────────────────────
map("n", "<leader>w", "<cmd>w<cr>", { desc = "ファイルを保存" })
map("n", "<leader>q", "<cmd>q<cr>", { desc = "ウィンドウを閉じる" })
map("n", "<leader>Q", "<cmd>qa<cr>", { desc = "全ウィンドウを閉じる" })

-- ── ペースト時にレジスタを保持 ──────────────────────────
map("x", "<leader>p", [["_dP]], { desc = "レジスタを保持してペースト" })

-- ── ターミナルモード ────────────────────────────────────
map("t", "<Esc><Esc>", "<C-\\><C-n>", { desc = "ターミナルのノーマルモードへ" })
map("t", "<C-h>", "<C-\\><C-n><C-w>h", { desc = "ターミナルから左ウィンドウへ" })
map("t", "<C-j>", "<C-\\><C-n><C-w>j", { desc = "ターミナルから下ウィンドウへ" })
map("t", "<C-k>", "<C-\\><C-n><C-w>k", { desc = "ターミナルから上ウィンドウへ" })
map("t", "<C-l>", "<C-\\><C-n><C-w>l", { desc = "ターミナルから右ウィンドウへ" })
